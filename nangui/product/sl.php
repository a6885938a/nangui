    LH型电动葫芦桥式起重机（简称葫双）系列，是以固式的电动葫芦（座式或挂式）作为起升机构（主钩和副钩），安装在小车上的桥式起重机。本系列产品设有地面操纵和司机室操纵两个品种，144个规格。
    该产品结构简单、重量轻、价格便宜，主要用于机械制造车间、仓库、装配及水电钻检修等场所的一般吊重、装卸作业。本产品的工业环境温度为-25～40℃。不适于用有爆炸危险、火灾危险的介质中和相对温度大于85%，充满腐蚀性气体的场所，也不适用于吊运溶化金属和有毒、易燃、易爆物品。
二、结构特征与工作原理
    本系列产品由桥架、小车、大车运行和电气四个主要部分组成。采用固定式的钢丝绳或链条电动葫芦作为起升机构。起重机装配试验应符合JB/T3695-2008《电动葫芦双梁桥式起重机》之规定，安装起重机时，必须按附图的规定进行安装。
    起重机在拆卸运输和产品安装时，端梁的连接必须按图纸规定拆装。
（一）、桥架
    桥架有主梁、端梁、走台、导电架、大车导电线挡架、栏杆等部件组成。
    本系列采用四梁结构，主梁与端梁用螺栓连接，非导电侧设一通长走台，小车馈电采用柔性扁电缆供电系统。
    为了防止小车行驶到极限位置时，吊具或钢丝绳与电源滑线相碰，在桥架的两根主梁间，靠近电源一端设置了导线挡架。
 （二）、小车
    本系列小车上的起升机构采用德马格钢丝绳电动葫芦，
    小车运行机构采用德马格驱动。
    小车架采用纵梁和横梁均由H型钢焊接制成，各梁之间采用焊接连接。
 （三）、大车
    大车运行机构采用分别驱动，采用SEW三合一减速电机RF系列。
 
